    public void readTable(){
        DefaultTableModel modelo = (DefaultTableModel) tituloTable.getModel();
        
        UsuarioDao table = new UsuarioDao();
        UsuarioDTO titulos = new UsuarioDTO();

        conn = new ConexaoDao().conectaBD();
        


        try {
            String sql = "SELECT * FROM livro ORDER BY nota DESC;";
            PreparedStatement pstm = conn.prepareStatement(sql);

            ResultSet rs = pstm.executeQuery();
            
            while(rs.next()){
                String nome = rs.getString("titulo");
                String nota = rs.getString("nota");
                String genero = rs.getString("id_categoria");
                
                titulos.setTitulo_livro(nome);
                titulos.setNota_livro(nota);
                modelo.addRow(new Object[]{nome,nota,genero});
                
//                System.out.println(nome+""+nota);
            
            }
            

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "UsuarioDao: " + erro);

        }
    }

