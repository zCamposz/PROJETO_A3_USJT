    private void enterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonActionPerformed
        // TODO add your handling code here:
        try {
            String titulo;

            titulo = selecionarTextField.getText();

            UsuarioDTO dtoUser = new UsuarioDTO();
            dtoUser.setTitulo_livro(titulo);
            
            UsuarioDao objusuariodao = new UsuarioDao();

            ResultSet rsusuariodao = objusuariodao.procurar(dtoUser);
            

            
            if(rsusuariodao.next()){
                Livro page = new Livro();
                page.exportaTest(dtoUser);
                page.setVisible(true);
                dispose();
            }
            
            else{
                JOptionPane.showMessageDialog(null, "Livro não existe");
            
            }
             
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Biblioteca "+erro);        
        }
    }//GEN-LAST:event_enterButtonActionPerformed

