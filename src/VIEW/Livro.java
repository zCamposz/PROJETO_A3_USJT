    public void exportaTest(UsuarioDTO user){
        nomeLabel.setText(user.getTitulo_livro());
        userLabel.setText("Bem vindo ");
        conn = new ConexaoDao().conectaBD();
        


        try {
            String sql = "SELECT * FROM livro WHERE titulo = ?";
            PreparedStatement pstm = conn.prepareStatement(sql);

            pstm.setString(1, user.getTitulo_livro());

            ResultSet rs = pstm.executeQuery();
            
            while(rs.next()){

                String nome = rs.getString("autor");
                String nota = rs.getString("nota");
                String genero = rs.getString("id_categoria");
                String descricao = rs.getString("descricao");
                
                user.setTitulo_livro(nome);
                user.setNota_livro(nota);
                user.setCategoria_livro(genero);
                user.setDescricao_livro(descricao);
                
                autorLabel.setText(user.getAutor_livro());
                notaLabel.setText(user.getNota_livro()+"/5");
                generoLabel.setText(user.getCategoria_livro());
                sinopseTextArea.setText(user.getDescricao_livro());
                sinopseTextArea.setEditable(false);
                

            
            }
            

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "UsuarioDao: " + erro);

        }
    
    }


